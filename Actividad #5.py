# El Mayor de Dos Numeros

number1 = float(input("Introduce un Numero: "))
number2 = float(input("Introduce un Numero: "))

if number1 > number2 :
    print(f"El Numero {number1} es Mayor")
elif number1 < number2 :
    print(f"EL Numero {number2} es Mayor")
else:
    print(f"Ambos Numeros son Iguales")
